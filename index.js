//Задача 1. 
//Напишите функцию, которая принимает 2 числа и возвращает 1, если первое число больше, чем второе; -1, если первое число меньше, чем второе, и 0, если числа равны.

function showNumber(a, b) {
    if (a > b) {
        console.log(1);
    } else if (a < b) {
        console.log(-1);
    } else if (a === b) {
        console.log(0);
    }
}

showNumber(5, 0);

//Попробуйте реализовать эту функцию не только с помощью if...else, но и с помощью тернарного оператора, и в виде стрелочной функции.   

let showNumberSecond = (a, b) => {
    return (a > b) ? '1' : (a < b) ? '-1' : '0';
}

console.log(showNumberSecond(0, 5));

