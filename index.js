//Напишите функцию, которая принимает 2 параметра: длину и ширину прямоугольника и вычисляет его площадь. Если в функцию передали 1 параметр, то функция вычисляет площадь квадрата.
//Подсказка: используйте параметры функции по умолчанию.    

let showSquare = (a, b = '') => {
    return (b === '') ? `Площадь квадрата: ${a ** 2}` : `Площадь прямоугольника: ${a * b}`;
}

console.log(showSquare(5));

//Задача 3
//Написать функцию, получающую на вход два числа. Если оба числа чётные - функция возвращает их произведение. Если оба числа нечётные - функция возвращает их сумму. 
//Если одно из чисел чётное, а второе нечётное - функция возвращает это нечётное число.

let showNumberThird = (a, b) => {
    return (b % 2 === 0 && a % 2 === 0) ? `Произведение: ${a * b}` : (b % 2 != 0 && a % 2 != 0) ? `Сумма: ${a + b}` : `Нечетное число: ${(b % 2 != 0) ? b : a}`;
}

console.log(showNumberThird(6, 7));

